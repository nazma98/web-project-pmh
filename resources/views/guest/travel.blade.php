             1. Traveling Improves Your Health
            </h3>
From cutting down on stress, to lowering your chances of developing a heart disease, the health benefits of traveling are huge. You may stay sitting on a chair all day long at the workplace: including some walking to your trip is sure to make your body feel better. For some people, wandering abroad is even a cure for depression and anxiety. Of course, it's not a foolproof cure, but it might help you feel better, both physically and psychologically. <br>

Traveling more is likely to have a tremendous impact on your mental well-being, especially if you're no used to going out of your comfort zone. Trust me: travel more and your doctor will be happy. Be sure to get in touch with your physician, they might recommend some medication to accompany you in your travels, especially if you're heading to regions of the globe with potentially dangerous diseases. <br><br>

<h3>
2. Traveling Lets You Disconnect From Your Daily Life
</h3>
This is closely related to my previous point. We tend to get so caught up in our daily lives that sometimes, by simply sticking around, we may do ourselves more harm
than good. Your boss is taking over your life? Kids are driving you mad? Your parents are trying to make you live the life they want? How long do you think you can handle this pressure before you burst and everything falls apart? <br>

Sometimes it is best to take a step back, take a deep breath and take go that Tower Bridge selfie. In all seriousness, travel is not a bad option - it is the most natural way of inducing the feeling you miss someone or that you are missed. The trick is to leave with a bit of preparation to avoid making a mistake during your journey. Plus, if you're flying, you better start thinking about booking your tickets sooner than later. <br><br>

<h3>
3. Traveling Makes You Smarter
</h3>
