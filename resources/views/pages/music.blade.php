                    <h1>Motivational Music</h1>
                    <p>
                    Music can be a powerful force. In fact, it is sometimes banned (along with steroids and stimulant drugs) to prevent athletes from gaining an unfair advantage during competitions. Why? Because of its capacity to energize and increase endurance.<br><br>

Elite types looking to win a big marathon prize may have to leave their ear buds at home. But the right tunes can be a great aid for those of us who still need a little push to get ourselves moving.<br><br>

<strong>How does music boost motivation so strongly and otherwise help get us through the most tedious of tasks?</strong><br><br>

One of music's energizing effects comes from its ability to engage the body's sympathetic nervous system. The activation of this system readies the body for action whenever we face a challenge in our environment.<br><br>

Airways open, the heart rate accelerates, and muscles are primed to move. Auditory signals – abrupt sounds or those that suddenly increase in frequency or volume – trigger alerting responses and increase physiological arousal.<br><br>

Music is comprised of these and other patterns of sounds that have been shown to affect levels of physical excitability. A simple increase in the pace of the music we listen to, for example, can quicken our pulse and accelerate our breathing. And this can be great for physical exercise or a boring task that might otherwise lull you to sleep.<br><br>

Music also affects the co-ordination of activity within and across different parts of the brain. Studies examining patterns of electrical activity across the brain suggest that synchronization of brain signals is important for linking perceptual, cognitive and motor processes.<br><br>

Recent results suggest further that a repetitive beat, such as that in a musical rhythm, synchronizes brainwaves in ways that may be particularly helpful in coupling what you hear with how you move. The co-ordination and execution of repetitive muscle movements may be made more efficient by matching the movements to a musical beat. This may explain why individuals strategically running to a beat run faster and use less oxygen than those who do not pace themselves with music.<br><br>

Physical and mental endurance can also be enhanced by music's capacity to draw our attention away from the negative aspects of a task. The brain's attention system, which includes some of the top and outermost regions of the frontal and parietal lobes, acts to enhance neural activity in areas that contribute to whatever we are focused on and reduce activity from other areas of the brain.<br><br>

Focusing on a favourite song combats de-motivating brain signals associated with fatigue or boredom. In the exercise domain, this has been shown to be particularly effective in moderately intense exercise. And while music does not seem to reduce the perceived strain of highly intense exercise, upbeat music has been shown to increase positive feelings about the intensity of the exercise compared to those of individuals in the same study who listened to less intense music.<br><br>

Indeed much of music's power lies in its ability to elicit emotional reactions and enhance mood. Recent neuroimaging investigations have allowed a dramatic increase in our understanding of how different networks of emotion- and motivation-related brain regions are recruited to produce these affective experiences – from the visceral shiver running down one's spine, to the sense of empowerment that can arise from a good set of lyrics, or intensely positive memories associated with a favourite piece of music.<br><br>

So the next time you face a daunting task, think of a strategic musical choice to give your brain and body a motivational boost.<br><br>



