            <h2>Get Started with Astrophotography</h2> <hr>
            <h4>Get a Good Camera</h4>
            
For the most part, astrophotography divides into two styles. There's long exposure shots, showing stars streaking across the sky in a dizzying array of circles. Then you have short exposure shots, which is your more traditional glamor photos of the Milky Way. <br>

To capture either one, you're going to need a camera that's better than an iPhone or a cheap point-and-shoot. That means you're looking for a DSLR with interchangeable lenses and manual controls. More expensive full-frame cameras have really great low-light sensitivity compared to tinier sensors on most other cameras, whether a pocket-friendly Canon or your run-of-the-mill smartphone. If you opt for a small sensor, your images will be plagued with noise and color shift—things that distract from the stellar beauty of your composition.
 <br>
<h4>Slow and Steady</h4>
When it comes to astrophotography, a tripod is a must, not an option. Creating these images can be a slow business compared to the blazing shutter speed of your average photo. If the cameras moves during an exposure, even a fraction of a fraction of an inch, your entire composition will likely be ruined. <br>

If you're in less than ideal conditions—say, it's windy—then you can weigh down your tripod by attaching your camera bag. You can also prevent camera shake by using the remote control or setting the camera to the self timer so the camera has time to settle after you press the shutter button, which can sometimes cause its own shaking. <br>
<h4>It's All About Technique</h4>
As with most things, the best way to become a stellar astrophotographer is to practice. Get out there, start snapping shots, and learn from your mistakes, which will be numerous at first. A few quick tips can help you scale that initial learning curve. <br>

Start by setting the camera to manual focus. The Rokinon lenses have markings on the lens to set the focus manually to infinity and usually works well for me. You can fine tune the focus by using the live view feature set to exposure simulation mode and zooming way into a bright star. If this doesn't work, try focusing on a friend standing in the distance (100 ft or more) holding a flashlight or headlamp. I've also set up the camera just before sunset and focused on a distant tree, doing this also helps get an idea of your composition if you plan to include trees or buildings in the shot. Of course, there is always trial and error, too, but can take a frustrating amount of time to get just right. <br>

