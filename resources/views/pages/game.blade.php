                    <h1>Why Play Is Important</h1>
                    <p>

From an evolutionary biology point of view, it's not immediately obvious why humans spend so much time on recreation and play. In humans, as well as other animals, time spent playing is time not spent looking for food. Play could be a distraction from looking out for predators. Play could cause needless injuries—even traumatic death. Viewed from the lens of species survival, the drive to play seems like it would be a huge disadvantage. But yet, all humans in all cultures like to play.<br><br>


The universality of play argues that it is an innate feature passed down from our ancestors. Furthermore, playing is not exactly a minor part of our lives. It is a big and important part of who we are. This behavior cannot simply be an evolutionary side effect or a genetic accident. We’re not talking about a tiny appendix in our abdomen. Play is a huge part of the human experience, especially among the young, and thus there must be value in it.<br><br>

Just like organs or tissues, behaviors will be carefully honed over time by natural selection. Behaviors that enhance survival and/or reproduction of individuals will develop through the generations and become part of the innate “nature” of the species. Such as the suckling of all infant mammals, many of our behavioral urges are driven purely by instinct.<br><br>

So what is the purpose or benefit of play? To understand our behaviors, it is often helpful to examining correlates in other animals. (That's the whole thrust of my book!) But do animals even engage in play?

<br><br>
Let's start with dogs. Undoubtedly, dogs are naturally given to wrestling with and chasing each other. However, rather than purely for fun, these behaviors could be attempts to establish dominance within a social context and a means to establish trust and familiarity. This doesn’t necessarily mean that these behaviors aren’t also fun for the dogs, but it does mean that we can’t claim that they are, per se, evidence that animals play just for the fun of it, like humans do. That’s the kind of play that we are talking about—just plain old having fun. That’s what we humans do. We just have fun. For humans, play is for play’s sake.<br><br>

Or is it? Maybe our definition of play is the problem. If we think of play as doing something enjoyable that has no other purpose, then of course we won’t find another purpose—we’ve made not having a purpose part of the definition. Instead, we need to keep an open mind about hidden purposes of play. We play because it is fun, but it may also be serving other purposes—that’s what we’re looking for.<br><br>

After all, if we discover biological benefits to playing, then it really isn’t “just for the fun of it.” It only seems that way. Perhaps the example of dogs playing together really is a clue to the function of play for other species, including us. Perhaps the secret to understanding the function of play is the realization that playful acts can be fun and serve some other purpose for the species.<br><br>

<strong>Defining Play</strong>

Defining play is a complicated business, but I find Gordon Burghardt's painstaking work to be the authority on the subject. He has convincingly established a five-point standard that well captures the essence of playful behavior. (He focuses on non-human animals but it holds for us as well.) For the purpose of this discussion, we can use the simple, though unscientific shorthand, "That which is fun/enjoyable but not other things like sex, eating, hunting, etc." When it comes to play and fun - we know it when we see it.
<br><br>
It's also important to remember that the "enjoyment" of fun is mostly secondary to its purpose. Pleasure is a neurological phenomenon that drives us to seek certain experiences that are good for our evolutionary future. Pleasure drives us to eat, have sex, wield power, and, of course, to play. Pleasure drives individual to do things, but that's just a trick. The goals of each of those things are wildly different. So the question is - why has evolution programmed us to enjoy seemingly pointless activities like playing baseball, doing puzzles, and engaging in make-believe?
                        <br><br>

<strong>The many benefits of different kinds of play</strong><br><br>

I spend most of the first chapter of my book summarizing many studies on the benefits of play for humans and other animals. I then analyze each of these "modalities" of play against the backdrop of our evolutionary history. Here are the highlights:<br><br>

1.) Play for establishing social rank. Although the image of animals fighting each other for dominance is common, all-out fights-to-death are rare in most species. Instead, wrestling and other feats of physical strength are used to establish the dominance hierarchy. It is safer to the individuals and better for group cohesion (in social-cooperative species).<br><br>

2.) Play for learning social rules. Many animals learn social etiquette and other important behaviors through playing. This involves correction, coercion, and reconciliation, but the benefit is social cohesion, orderly hierarchy, and cooperation for mutual aid. Animal behaviorist Marc Bekoff, a leading expert on animal play, has extensively studied its important role in the social fabric of wolves.<br><br>

3.) Play as practice. This idea is that some forms of play serve as a warm-up for doing the real thing as adults. Cats play-hunt inanimate objects and some monkeys engage in play-mothering as juveniles. The jury is out whether play-practice actually helps or is merely an outlet for the instincts that will take shape as the animals (including humans) mature.<br><br>

4.) Play to establish motor coordination. Fine control of muscle movements requires a lot of practice and feedback training. The fun of play drives animals to stay active and to especially practice certain kinds of movement that helps them develop their motor skills.<br><br>

5.) Play as social bonding. In social animals, play helps to promote bonding among group members, relieve in-group tension, and establish trust. The group that plays together stays together.<br><br>



6.) Play for managing stress. Humans aren't the only animals that get stressed out. Certain forms of play serve to lower stress hormones and, in so doing, promote mental and physical health.<br><br>

7.) Play for developing certain cognitive and creative skills. Separate from fine-motor control and the practicing of certain tasks or skills, playing can help in the general development of our brains and minds. It can encourage creativity, planning, problem solving, and a whole bunch of task-specific skills like spatial reasoning and logic. For both humans and animals, play is a low-risk way to develop our cognitive abilities.<br><br>

There are certainly more, but I discuss these benefits of play because they have solid research studies with both humans and animals that explore the mechanisms and benefits. Humans are a playful species, as are all animals.<br><br>

One of the most important features of play is that it is only engaged when an animal is in a "relaxed field," meaning they are adequately fed, healthy, and not under imminent predator threat. This is why we see animals playing in captivity more than we do in the wild, since they are relatively protected and their temporal needs are met. When animals in captivity are NOT playful, we know that they are stressed and the stress may come from the captivity itself.<br><br>

Make sure you make some time to play today!<br><br>
                        <strong>Nathan H. Lents, Ph.D.<br>
Beastly Behavior</strong>
